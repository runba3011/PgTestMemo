public class ArithmenticOperator {
  public static void main(String[] args) {
    int a = 12;
    int b = 5;
    System.out.printf("a = %d , b = %d%n%n", a, b);
    System.out.printf("a + b = %d%n", a + b);
    System.out.printf("a - b = %d%n", a - b);
    System.out.printf("a * b = %d%n", a * b);
    System.out.printf("a / b = %d%n", a / b);
    System.out.printf("a %% b = %d%n", a % b);
  }
}
